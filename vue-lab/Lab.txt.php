Лабораторна робота: Vue 3 + TypeScript + Vue Router

Кроки, які виконано:

1. Створено новий проєкт Vue 3 за допомогою `npm create vue@latest vue-lab`.
2. Інтегровано TypeScript під час створення проєкту.
3. Налаштовано Vue Router для навігації між сторінками.
4. Розглянуто структуру проєкту:
- src/main.ts (вхідна точка)
- src/router/index.ts (налаштування маршрутів)
- src/App.vue (кореневий компонент)
- src/views/ (сторінки)
- src/components/ (компоненти)
5. Додано нову сторінку "Contact" (/contact) і зареєстровано її у маршрутизаторі.
6. Створено компонент UserCard.vue з типізованими пропсами (username: string, isActive: boolean).
7. Використано компонент UserCard на сторінці ContactView.vue для відображення користувачів.

Текст помилки при спробі передати число замість boolean (коментарем у ContactView.vue):
// Error: Type 'number' is not assignable to type 'boolean'.

Висновки:
- Використання TypeScript дозволяє отримати чітку типізацію даних і уникнути помилок під час розробки.
- Vue Router забезпечує швидку навігацію між сторінками без перезавантаження браузера.
- Компонентна структура дозволяє повторно використовувати код і підтримувати чистоту проєкту.


Звіт по Tailwind CSS та UserCard.vue

1. Інсталяція та налаштування Tailwind CSS

Я встановила Tailwind CSS у проект Vue за допомогою npm.

Налаштувала tailwind.config.js для темної теми (darkMode: 'class').

Додала Tailwind у головний файл стилів (@tailwind base; @tailwind components; @tailwind utilities;).

2. Реалізація умовних стилів у UserCard.vue

Використала пропс isActive для визначення стану користувача.

Динамічно змінювала класи Tailwind через :class:

Активний стан → зелена рамка і світлий фон.

Неактивний стан → сірі кольори.

Для темної теми додала класи dark:bg-*, dark:border-*, dark:text-*.

3. Переваги utility-first підходу (Tailwind)

Швидка розробка інтерфейсу.

Легко додавати адаптивність і темну тему.

Мінімум дублювання CSS і відсутність глобальних конфліктів.

4. Недоліки Tailwind

HTML може стати захаращеним великою кількістю класів.

Для складних стилів іноді потрібен додатковий CSS.

5. Бонус: Темна тема

Компонент підтримує темну тему.

При активації класу dark фон, рамка і текст автоматично змінюються.